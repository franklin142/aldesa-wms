package org.aldesa.wms.model;

public class OpcionMenu {
	private String opcionesMenu;
	private String codigoModulo;
	private String descripcion;
	private String codSisema;
	
	public String getOpcionesMenu() {
		return opcionesMenu;
	}
	public void setOpcionesMenu(String opcionesMenu) {
		this.opcionesMenu = opcionesMenu;
	}
	public String getCodigoModulo() {
		return codigoModulo;
	}
	public void setCodigoModulo(String codigoModulo) {
		this.codigoModulo = codigoModulo;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getCodSisema() {
		return codSisema;
	}
	public void setCodSisema(String codSisema) {
		this.codSisema = codSisema;
	}
}
